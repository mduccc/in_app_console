/// Enum for the type of in app logger.
///
/// This enum is used to determine the type of in app logger.
///
/// [info] is used for info messages.
/// [error] is used for error messages.
/// [warning] is used for warning messages.
///
enum InAppLoggerType {
  /// Info message.
  info,

  /// Error message.
  error,

  /// Warning message.
  warning,
}
